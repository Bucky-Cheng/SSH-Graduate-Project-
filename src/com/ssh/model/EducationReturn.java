package com.ssh.model;

public class EducationReturn {

	private int ID;
	private String td;
	private String data;
	
	public EducationReturn(){
		
	}

	
	public int getID() {
		return ID;
	}


	public void setID(int iD) {
		ID = iD;
	}


	public String getTd() {
		return td;
	}

	public void setTd(String td) {
		this.td = td;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}
	
}
