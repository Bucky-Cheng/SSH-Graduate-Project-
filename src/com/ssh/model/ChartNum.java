package com.ssh.model;

public class ChartNum{
	
	private int ID;
	private int Teacher;
	private int Instructor;
	private int Assistant;
	private int Administrator;
	private int Complited;
	private int Uncomplited;
	public ChartNum(){
		
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getTeacher() {
		return Teacher;
	}
	public void setTeacher(int teacher) {
		Teacher = teacher;
	}
	public int getInstructor() {
		return Instructor;
	}
	public void setInstructor(int instructor) {
		Instructor = instructor;
	}
	public int getAssistant() {
		return Assistant;
	}
	public void setAssistant(int assistant) {
		Assistant = assistant;
	}
	public int getAdministrator() {
		return Administrator;
	}
	public void setAdministrator(int administrator) {
		Administrator = administrator;
	}
	public int getComplited() {
		return Complited;
	}
	public void setComplited(int complited) {
		Complited = complited;
	}
	public int getUncomplited() {
		return Uncomplited;
	}
	public void setUncomplited(int uncomplited) {
		Uncomplited = uncomplited;
	}
	
	
	
}
